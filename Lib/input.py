# Input
# Input() method allows user to input, makes it interactive
# Example
Name = input("Please enter your name: ")
Age = input("What is your age: ")

print("Your Name is: ", Name ," and your age is: ", Age)

# We can make a parameter which can accept only (string,integers, and float)
# String
String_type = str(input("Type only string type: "))
print("Your output :" + String_type)

# Note : String accepts all (strings, integer nor float)

# Integer
Int_type = int(input("Enter random numbers: "))
print("Numbers you type: ")

# Note : Interger accepts whole numbers and float (it will converted into integer type)

# Float 
Float_type = float(input("Enter decimal numbers: "))
print("Decimals you type: " + Float_type)

# Note : It only accept decimal numbers



