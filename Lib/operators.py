# Basic Operators
# Python use a basic operators such as :
# Addition( + ), Subtraction ( - ), Multiplication ( * ), Division ( / )
# Floor Division ( // ),  Modulus ( % ), Exponent ( ** )
# For example 
x = 5
y = 2

# Addition 
x + y 
print(x + y)

# Subraction
x - y
print(x - y)

# Multiplication
x * y
print(x * y)

# Division 
x / y
print(x / y)

# Floor Division 
# It rounds down to the nearest whole number
x // y
print( x // y)

# Modulus 
# It gives a remainder 
x % y 
print(x % y)

# Exponent
# It raises the x to the y
x ** y
print(x ** y)

# More Assingment Operators
# Besides of = sign, we have four more operators in python
# " += ", " -= ", " *= "
# For example 

x = 10
x = x + 2
print(x)
# The initial value of our 'x' is 10 and we want to increment to 2 
# So our value of our 'x' will be ( x <- 12 )

x = 10
x = x - 2
print(x)
# The value of our 'x' is 10 and we will subtract it by 2
# So our value of our 'x' is ( x <- 8 )

x = 10
x = x * 2
print(x)
# The value of our 'x' is 10 and we will be multiply it by 2
# So our value of our 'x' is ( x <- 20 )

